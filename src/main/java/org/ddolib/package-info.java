/**
 * Main package of the framework. The structure is the following:
 *
 * <ul>
 *     <li>
 *          The {@code acs} package contains the framework you will be using to complete your
 *          Acs solver with MDD algorithm.
 *    </li>
 *     <li>
 *          The {@code astar} package contains the framework you will be using to complete your
 *          As star (A*) solver with MDD algorithm.
 *    </li>
 *     <li>
 *         The {@code common} package contains interfaces and class that can be used for all kind
 *         of solvers (ddo, astar, acs)
 *     </li>
 *     <li>
 *         The {@code ddo} package composes the framework you will be using to complete your
 *         assignment on the Branch-and-Bound with MDD algorithm.
 *     </li>
 *     <li>
 *          The {@code examples} package contains the examples on some classic problems on witch the different solver
 *          are applied on.
 *    </li>
 *     <li>
 *          The {@code modeling} package contains interfaces and abstract classes to extend for
 *          implementing your optimization problem.
 *     </li>
 *     <li>
 *         The {@code util} package contains class of tools used in the different solvers.
 *     </li>
 *     <li>
 *         The {@code factory} package provides a user-friendly api, e.g. factory for solvers with
 *         default values.
 *     </li>
 * </ul>
 */
package org.ddolib;